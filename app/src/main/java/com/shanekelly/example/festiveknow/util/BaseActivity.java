package com.shanekelly.example.festiveknow.util;

import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentManager;
import androidx.fragment.app.FragmentTransaction;
import androidx.appcompat.app.AppCompatActivity;

//BaseActivity is mainly responsible for adding Fragments to Activities
//this is an older way of implementing fragments, however I wanted to discover how fragments where originally created.
public abstract class BaseActivity extends AppCompatActivity {


    public void addFragmentToActivity (FragmentManager fragmentManager,
                                              Fragment fragment,
                                              int frameId,
                                              String tag) {

        FragmentTransaction transaction = fragmentManager.beginTransaction();
        transaction.replace(frameId, fragment, tag);
        transaction.commit();
    }
}
